package com.objetos;

import java.util.Date;

public class AnimalCuadrupedo {
	
	private float altura;
	private float peso;
	private int edadAprox;
	private Date nacimiento; 
	private String razgos;
	private char sexo;
	private String color;
	private String raza;
	private String especie;
	
	public AnimalCuadrupedo(float altura, float peso, int edadAprox, Date nacimiento, String razgos, char sexo,
			String color, String raza, String especie) {
		
		this.altura = altura;
		this.peso = peso;
		this.edadAprox = edadAprox;
		this.nacimiento = nacimiento;
		this.razgos = razgos;
		this.sexo = sexo;
		this.color = color;
		this.raza = raza;
		this.especie = especie;
	}
	
	

}

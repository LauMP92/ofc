package com.objetos;

import java.util.Date;

public class Hombre {
	private float altura;
	private float peso;
	private int edad;
	private Date nacimiento; 
	private String nacionalidad;
	private String razgos;
	private float dni;
	
	
	public Hombre(float altura, float peso, int edad, Date nacimiento, String nacionalidad, String razgos, float dni) {
		
		this.altura = altura;
		this.peso = peso;
		this.edad = edad;
		this.nacimiento = nacimiento;
		this.nacionalidad = nacionalidad;
		this.razgos = razgos;
		this.dni = dni;
	}
	
	

}

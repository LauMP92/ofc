package com.objetos.model;

import java.util.Date;

public class Hombre {
	private float altura;
	private float peso;
	private int edad;
	private String rasgos;
	private Date nacimiento;
	
	public Hombre(float altura, float peso, int edad, String rasgos,Date nacimiento) {
		
		this.altura = altura;
		this.peso = peso;
		this.edad = edad;
		this.rasgos = rasgos;
		this.nacimiento = nacimiento;
	}
	
	
	
}
